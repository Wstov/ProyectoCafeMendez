                                          safeSetState(() {});
                                          return;
                                        }
                                      }

                                      await widget
                                          .parametrosProducto!.reference
                                          .update(createProductosRecordData(
                                        imagen: _model.uploadedFileUrl,
                                      ));
                                    },
                                    child: Icon(
                                      Icons.upload,
